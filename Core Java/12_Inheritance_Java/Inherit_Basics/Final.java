import java.util.*;

// Use Case of the Final Keyword --> In java 
// Finala Keyword in java are used to Declare a Given Variable as a constant 
// --> Meaning its Value wont change after declaration using the keyword Final 
public class Final {
    public static void main(String[] args) {
        final double  x =3.1454;
        System.out.println(x);
        // x=4.545454; --> Throws A Runtime Error
    }
}
